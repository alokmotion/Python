# procedural programming Vs Object Oriented Programming 


def add(a,b):
    return a+b

def subtract(a,b):
    return a-b

def main(x,y):
    print("sum", add(x,y))
    print("sub", subtract(x,y))


x = 10
y = 20

main(x,y)



